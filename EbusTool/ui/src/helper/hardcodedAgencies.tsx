export function getHardCodedAgencies(): Agency[] {
  //csv headings will be: 
  // [0] uniqueId, 
  // [1]state, 
  // [2]state_name, 
  // [3]region_name,  
  // [4]date_accessed,  
  // [5]agency_id,  
  // [6]agency_name,  
  // [7]vehicle_types

  // csv headings are:
  // [0]:state	[1]:state_name	[2]:region [3]:region_name [4]:agency_id [5]:agency_name [6]vehicle_types

  // Note: The agency_id in this instance is not necessarily a unique identifier of a row.

  const agencies = [];
  let  uniqueId = 0;
  for (const row of PlaceholderAgenciesString.split('\n')) {
    const cells = row.split(',');
    const state = cells[0].toUpperCase() as AussieState;
    const region = cells[2];
    const id = cells[2] + '_' + cells[4] + '_' + cells[5];
    const name = cells[3] + ' - ' + cells[4] + ' - ' + cells[5];
    // const date_accessed = cells[4];
    const vehicles = row.split('[')[1];
    const validVehicleTypes = [];

    if (vehicles.includes('Bus')) validVehicleTypes.push('Bus');
    if (vehicles.includes('Ferry')) validVehicleTypes.push('Ferry');

    agencies.push({agencyId: cells[4], 
      region: region, 
      dateAccessed: 'someDate', 
      name: name, 
      id: ''+uniqueId, 
      state: state, 
      vehicleTypes: validVehicleTypes});
    uniqueId++;
  }

  return agencies;
}


